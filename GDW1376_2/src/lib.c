/**
 ******************************************************************************
 * @brief   txt瘻buf렘랬
 * @param[in]  *pin : 渴흙txt匡굶
 * @param[in]  inlen: 渴흙txt匡굶낀똑
 * @param[out] *pout: 渴놔괩匡
 * @param[in]  olen : 渴놔괩匡뻠닸낀똑
 *
 * @retval 瘻뻣빈buf돨낀똑
 *
 * daemo: "1asbsaaa123456478923" --> 1A BA AA 12 34 56 47 89 23
 ******************************************************************************
 */
int
txt_to_buf(const char *pin,
        int inlen,
        unsigned char  *pout,
        int olen)
{
    unsigned char v;
    int rlen = 0;

    olen <<= 1;

    while (inlen-- && (olen > rlen))
    {
        if ((*pin >= '0') && (*pin <= '9'))
        {
            v = *pin - '0';
        }
        else if ((*pin >= 'a') && (*pin <= 'f'))
        {
            v = *pin - 'a' + 10;
        }
        else if ((*pin >= 'A') && (*pin <= 'F'))
        {
            v = *pin - 'A' + 10;
        }
        else
        {
            pin++;
            continue;
        }

        pout[rlen >> 1] = (rlen & 1) ? ((pout[rlen >> 1] << 4) | v) : v;
        rlen++;
        pin++;
    }

    return (rlen + 1) >> 1;
}

