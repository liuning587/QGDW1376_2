/**
 ******************************************************************************
 * @brief   셕炬fn
 * @param[in]  DT1 :
 * @param[in]  DT2 :
 *
 * @retval  fn
 * @note    董珂怜連넣1몸fn
 ******************************************************************************
 */
int
get_fn(unsigned char DT1,
        unsigned char DT2)
{
    int i;

    for (i = 7; i >= 0; i--)
    {
        if (BITS(DT1, i))
        {
            return (DT2 * 8) + i + 1;
        }
    }

    return 0;
}

/**
 ******************************************************************************
 * @brief   셕炬fn
 * @param[in]  *DT1 :
 * @param[in]  *DT2 :
 *
 * @retval  fn
 * @note    董珂怜連넣1몸fn
 ******************************************************************************
 */
int
get_clear_fn(unsigned char *DT1,
        unsigned char *DT2)
{
    int i;

    for (i = 7; i >= 0; i--)
    {
        if (BITS(*DT1, i))
        {
//            SETBITS(*DT1, i, 0); //헌뇜깃羚
            return (*DT2 * 8) + i + 1;
        }
    }

    return 0;
}

