unsigned char
get_cs(const unsigned char *pfbuf,
        unsigned int len)
{
    unsigned char cs = 0u;
    int i;

    for (i = 0; i < len; i++)
    {
        cs = cs + pfbuf[i];
    }

    return cs;
}
